# formas de se imprimir um texto na tela
#
# formatando igual linguagem C
nome = 'everton'
print('Olá %s parabéns pelo seu primeiro Hallo Word' % nome)
# formatando com o format()
print('Olá {} parabéns pelo seu primeiro Hallo Word'.format(nome))
# formatando com a string declara dentro do print
print(f'Olá {nome} parabéns pelo seu primeiro Hallo Word')
print('você não viu mas no codigo esta todos diferentes')
